<h2> Chords theory</h2>

<p> Chords are combinations of notes played together, forming the harmonic foundation of music.  </p>

<p>There are several types of chords with three or four notes, and several inversions:</p>

<p>Three-note chords: (formed from a root, a third, and a fifth)</p>
<p>
    <ul>
        <li>Diminished: 3m + 3m = 5d</li>
        <li>Minor: 3m + 3M = 5J</li>
        <li>Major: 3M + 3m = 5J</li>
        <li>Augmented: 3M + 3M = 5a</li>
    </ul>
</p>

<img src="../pictures/chords_types_3.svg" alt="Chords types"  height="100">

<p>Inversions of three-note chords:</p>
<p>
    <ul>
        <li>Root Position: 3 + 3 = 5 (the root is in the bass)</li>
        <li>First Inversion: 3 + 4 = 6 (the third is in the bass)</li>
        <li>Second Inversion: 4 + 3 = 6 (the fifth is in the bass)</li>
    </ul>
</p>

<img src="../pictures/chords_inversions_1.svg" alt="Chords inversions"  height="100"> <br>
<img src="../pictures/chords_inversions_2.svg" alt="Chords inversions"  height="100">

<p>Four-note chords: (formed from a root, a third, a fifth, and a seventh)</p>
<p>
    <ul>
        <li>Diminished: 3m + 3m + 3m = 7d</li>
        <li>Dominant: 3M + 3m + 3m = 7m</li>
    </ul>
</p>

