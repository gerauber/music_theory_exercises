
<p>There are several cadences using different degrees of scales:</p>

<ul>
    <li>Authentic / Perfect Cadence: Degrees V then I</li>
    <li>Plagal Cadence: Degrees IV then I</li>
    <li>Interrupted / Deceptive Cadence: Degrees V then VI</li>
    <li>Imperfect / Half Cadence: Degrees I or II or IV then V</li>
</ul>

