
<p>Rhythm is categorized mainly into binary and ternary rhythms.</p>

<p>Binary rhythms are based on groupings of two.</p>
<ul>
    <li>Characteristics: Grouped in twos, common in pop and rock music.</li>
    <li>Examples:
        <ul>
            <li>4/4 Time</li>
            <li>2/4 Time</li>
            <li>6/8 Time (feels binary)</li>
        </ul>
    </li>
</ul>

