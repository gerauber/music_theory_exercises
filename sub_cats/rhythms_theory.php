<p>Ternary rhythms are based on groupings of three.</p>
<ul>
    <li>Characteristics: Grouped in threes, common in waltz and jazz.</li>
    <li>Examples:
        <ul>
            <li>3/4 Time</li>
            <li>6/8 Time (feels ternary)</li>
            <li>9/8 Time</li>
        </ul>
    </li>
</ul>

